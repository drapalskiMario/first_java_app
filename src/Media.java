public class Media {
    public static void main(String[] args) {
        double nota1 = 7.8;
        double nota2 = 9.0;
        double nota3 = 6.7;
        double nota4 = 8.0;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println("A média das 4 notas é: " + media);
    }
}